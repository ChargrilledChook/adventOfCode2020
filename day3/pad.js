function tobogganSlide(treeArray, rightDir, downDir, tree) {
  let result = 0;
  let position = 0;
  let len = treeArray[0].length;
  for (let i = 0; i < treeArray.length; i += downDir) {
    if (treeArray[i][position] === tree) result++;
    position = (position + rightDir) % len;
  }
  return result;
}

